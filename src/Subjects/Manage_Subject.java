    public void upDateDB() {
        
        try{
            con = DBClass.getConnection();
            pst = con.prepareStatement("select * from subjects");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)TableSub.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                Vector columnData = new Vector();
                
                for (i = 1;i <= q;i++){
                    columnData.add(rs.getString("idSubjects"));
                    columnData.add(rs.getString("subjectName"));
                    columnData.add(rs.getString("subjectCode"));
                    columnData.add(rs.getString("year"));
                    columnData.add(rs.getString("semester"));
                    columnData.add(rs.getString("lecHours"));
                    columnData.add(rs.getString("tuteHours"));
                    columnData.add(rs.getString("labHours"));
                    columnData.add(rs.getString("eveHours"));
                } 
                RecordTable.addRow(columnData);
            }
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }    
      
