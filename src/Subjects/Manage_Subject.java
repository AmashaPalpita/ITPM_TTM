    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)TableSub.getModel();
        int SelectedRows = TableSub.getSelectedRow();
        
        try {
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            con = DBClass.getConnection();
            
            pst = con.prepareStatement("update subjects set subjectName = ?, subjectCode = ?, year = ?, semester = ?, lecHours = ?, tuteHours = ?, labHours = ?, eveHours = ? where idSubjects = ?");
            
                     
        pst.setString(1, SN.getText());
        pst.setString(2, SC.getText());
        pst.setString(3, (String) year.getSelectedItem());
        pst.setString(4, (String) semester.getSelectedItem());
        pst.setString(5, (String) LH.getSelectedItem());
        pst.setString(6, (String) TH.getSelectedItem());
        pst.setString(7, (String) PH.getSelectedItem());
        pst.setString(8, (String) EH.getSelectedItem());
        pst.setInt(9, id);
        
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Updated Successfully");
        upDateDB();
        
        SN.setText("");
        SC.setText("");
        year.setSelectedIndex(0);
        semester.setSelectedIndex(0);
        LH.setSelectedIndex(0);
        TH.setSelectedIndex(0);
        PH.setSelectedIndex(0);
        EH.setSelectedIndex(0);
        
        } catch (SQLException e) {
         Logger.getLogger(Manage_Lecturer.class.getName()).log(Level.SEVERE, null, e);
        }                                                
    }//GEN-LAST:event_updateButtonActionPerformed

