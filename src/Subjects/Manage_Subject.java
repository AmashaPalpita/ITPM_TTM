    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)TableSub.getModel();
        int SelectedRows = TableSub.getSelectedRow();
        
        try {
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            
            deleteItem = JOptionPane.showConfirmDialog(null, "Do you want to delete?", "Warning",JOptionPane.YES_NO_OPTION);
            
            if (deleteItem == JOptionPane.YES_OPTION) {
                con = DBClass.getConnection();
                pst = con.prepareStatement("delete from subjects where idSubjects = ?");
                
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Deleted Successfully");
                upDateDB();
                
                SN.setText("");
                SC.setText("");
                year.setSelectedIndex(0);
                semester.setSelectedIndex(0);
                LH.setSelectedIndex(0);
                TH.setSelectedIndex(0);
                PH.setSelectedIndex(0);
                EH.setSelectedIndex(0);
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

