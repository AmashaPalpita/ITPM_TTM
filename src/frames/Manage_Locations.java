    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int r =jTable1.getSelectedRow();
        String ID = jTable1.getValueAt(r, 0).toString();
        String bulding = jTable1.getValueAt(r, 1).toString();
        String room = jTable1.getValueAt(r, 2).toString();
        String r_type = jTable1.getValueAt(r, 3).toString();
        String capa = jTable1.getValueAt(r, 4).toString();
        
        Id.setText(ID);
        buldingN.setText(bulding);
        roomN.setText(room);
        if(r_type.equals("Lecture Hall")){
            LecHall.setSelected(true);
        }
        else{
            Lab.setSelected(true);
        }
        cap.setText(capa);
        
    }//GEN-LAST:event_jTable1MouseClicked

