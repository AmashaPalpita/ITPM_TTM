    private void updateMLActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateMLActionPerformed
       int x = JOptionPane.showConfirmDialog(null, "Do you Really want to update");
        
        if(x==0){
            LecHall.setActionCommand("Lecture Hall");
            Lab.setActionCommand("Laboratory");
            
            String ID = Id.getText();
            String bulding = buldingN.getText();
            String room = roomN.getText();
            String r_type = buttonGroup1.getSelection().getActionCommand();
            String capa = cap.getText();
            
             try{

                String q="UPDATE locations SET bulding='"+bulding+"',room='"+room+"',r_type='"+r_type+"',capa='"+capa+"' WHERE ID ='"+ID+"' ";
                pst=con.prepareStatement(q);
                pst.execute();
                 
                tableload();
           
            }catch(Exception e){

            }
            
        }
    }//GEN-LAST:event_updateMLActionPerformed

