    public void upDateDB() {
        
        try{
            con = DBClass.getConnection();
            pst = con.prepareStatement("select * from lecturer");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData = rs.getMetaData();
            
            q = stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)TableLec.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                Vector columnData = new Vector();
                
                for (i = 1;i <= q;i++){
                    columnData.add(rs.getString("idLecturer"));
                    columnData.add(rs.getString("lecturerName"));
                    columnData.add(rs.getString("employeeID"));
                    columnData.add(rs.getString("faculty"));
                    columnData.add(rs.getString("department"));
                    columnData.add(rs.getString("center"));
                    columnData.add(rs.getString("building"));
                    columnData.add(rs.getString("level"));
                    columnData.add(rs.getString("ranks"));
                } 
                RecordTable.addRow(columnData);
            }
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }
