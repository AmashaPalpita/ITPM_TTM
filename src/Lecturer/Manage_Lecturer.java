    private void UpdateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)TableLec.getModel();
        int SelectedRows = TableLec.getSelectedRow();
        
        try {
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            con = DBClass.getConnection();
            
            pst = con.prepareStatement("update lecturer set lecturerName = ?, employeeID = ?, faculty = ?, department = ?, center = ?, building = ?, level = ?, ranks = ? where idLecturer = ?");
            
                     
        pst.setString(1, lname.getText());
        pst.setString(2, empid.getText());
        pst.setString(3, (String) faculty.getSelectedItem());
        pst.setString(4, (String) dept.getSelectedItem());
        pst.setString(5, (String) center.getSelectedItem());
        pst.setString(6, (String) building.getSelectedItem());
        pst.setString(7, (String) level.getSelectedItem());
        
        String a = empid.getText();
        String b = (String) level.getSelectedItem();
        ranks.setText(b+"."+a);
        pst.setString(8, ranks.getText());
        pst.setInt(9, id);
        
        
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Updated Successfully");
        upDateDB();
        
        lname.setText("");
        empid.setText("");
        faculty.setSelectedIndex(0);
        dept.setSelectedIndex(0);
        center.setSelectedIndex(0);
        building.setSelectedIndex(0);
        level.setSelectedIndex(0);
        ranks.setText("");
        
        } catch (SQLException e) {
         Logger.getLogger(Manage_Lecturer.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_UpdateButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        DefaultTableModel RecordTable = (DefaultTableModel)TableLec.getModel();
        int SelectedRows = TableLec.getSelectedRow();
        
        try {
            id = Integer.parseInt(RecordTable.getValueAt(SelectedRows, 0).toString());
            
            deleteItem = JOptionPane.showConfirmDialog(null, "Do you want to delete?", "Warning",JOptionPane.YES_NO_OPTION);
            
            if (deleteItem == JOptionPane.YES_OPTION) {
                con = DBClass.getConnection();
                pst = con.prepareStatement("delete from lecturer where idLecturer = ?");
                
                pst.setInt(1, id);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Deleted Successfully");
                upDateDB();
                
                lname.setText("");
                empid.setText("");
                faculty.setSelectedIndex(0);
                dept.setSelectedIndex(0);
                center.setSelectedIndex(0);
                building.setSelectedIndex(0);
                level.setSelectedIndex(0);
                ranks.setText("");
            }
        } catch (SQLException e) {
            System.err.println(e);
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

